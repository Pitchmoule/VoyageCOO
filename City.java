

public class City {

		public String name;
		public  int id;
		
		
		public City(){}
		
		public City(String aCity, int id){
			this.name = aCity;
			this.id = id;
		}

		public String getName() {
			return name;
		}

		public void setName(String name) {
			this.name = name;
		}


		public int getId() {
			return this.id;
		}

			
	}

